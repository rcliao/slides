/**
 * Render text as block-font banner (5 lines tall).
 * Returns array of 5 strings. Characters not in font are skipped.
 */
export function renderBlockText(text: string): string[] {
  const upper = text.toUpperCase();
  const rows: string[][] = [[], [], [], [], []];

  for (const ch of upper) {
    const glyph = GLYPHS[ch];
    if (!glyph) continue;
    for (let r = 0; r < 5; r++) {
      rows[r].push(glyph[r]);
    }
  }

  return rows.map(r => r.join(' '));
}

/**
 * Get the visual width of a block-text rendering.
 */
export function blockTextWidth(text: string): number {
  const lines = renderBlockText(text);
  return Math.max(...lines.map(l => l.length));
}
